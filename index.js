$( document ).ready(function() {

    let envelope = $('#envelope');
    let btn_open = $("#open");
    let btn_reset = $("#reset");


    envelope.click( function() {
        open();
    });
    btn_open.click( function() {
        open();
    });
    btn_reset.click( function() {
        close();
    });

    function open() {
        envelope.addClass("open")
           .removeClass("close");
    }
    function close() {
        envelope.addClass("close")
           .removeClass("open");
    }

});